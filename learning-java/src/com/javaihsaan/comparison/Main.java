package com.javaihsaan.comparison;

public class Main {
    public static void main(String[] args) throws Exception {
        int hossamAge = 28;

        int zubeiraAge = 28;

        System.out.println(zubeiraAge > hossamAge);
        System.out.println(zubeiraAge >= hossamAge);
        System.out.println(zubeiraAge < hossamAge);
        System.out.println(zubeiraAge <= hossamAge);
        System.out.println(zubeiraAge == hossamAge);
        System.out.println(zubeiraAge != hossamAge);
    }
}
